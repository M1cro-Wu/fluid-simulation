function [A, B, C] = stokes_fifth_coefficients(k, d)
% 
% [A, B, C] = stokes_fifth_coefficients(k, d)
%
% INPUTS
% ------------------------------------------------------------
% k = wavenumber [rad/m]
% d = water depth [m]
% ------------------------------------------------------------
%
% OUTPUTS
% ------------------------------------------------------------
% A = Stokes fifth A coefficients
% B = Stokes fifth B coefficients
% C = Stokes fifth C coefficients. Note that C(1) = C0, C(2) = C2 and C(4) = C4;
% ------------------------------------------------------------
% 

% Depth dependence
S = sech(2*k*d);
    
% A coefficients
A = nan(5,5); % Initialise as NaN
A(1,1) = 1/sinh(k*d);
A(2,2) = 3*(S^2)/(2*((1-S)^2));
A(1,3) = (-4-20*S+10*(S^2)-13*(S^3))/(8*sinh(k*d)*((1-S)^3));
A(3,3) = (-2*(S^2)+11*(S^3))/(8*sinh(k*d)*((1-S)^3));
A(2,4) = (12*S-14*(S^2)-264*(S^3)-45*(S^4)-13*(S^5))/(24*((1-S)^5));
A(4,4) = (10*(S^3)-174*(S^4)+291*(S^5)+278*(S^6))/(48*(3+2*S)*((1-S)^5));
A(1,5) = (-1184+32*S+13232*(S^2)+21712*(S^3)+20940*(S^4)+12554*(S^5)-500*(S^6)-3341*(S^7)-670*(S^8))/(64*sinh(k*d)*(3+2*S)*(4+S)*((1-S)^6));
A(3,5) = (4*S+105*(S^2)+198*(S^3)-1376*(S^4)-1302*(S^5)-117*(S^6)+58*(S^7))/(32*sinh(k*d)*(3+2*S)*((1-S)^6));
A(5,5) = (-6*(S^3)+272*(S^4)-1552*(S^5)+852*(S^6)+2029*(S^7)+430*(S^8))/(64*sinh(k*d)*(3+2*S)*(4+S)*((1-S)^6));
    

% B coefficients
B = nan(5,5); % Initialise as NaN
B(2,2) = coth(k*d)*(1+2*S)/(2*(1-S));
B(3,1) = -3*(1+3*S+3*S^2+2*S^3)/(8*(1-S)^3);
B(4,2) = coth(k*d)*(6-26*S-182*S^2-204*S^3-25*S^4+26*S^5)/(6*(3+2*S)*(1-S)^4);
B(4,4) = coth(k*d)*(24+92*S+122*S^2+66*S^3+67*S^4+34*S^5)/(24*(3+2*S)*(1-S)^4);
B(5,3) = 9*(132+17*S-2216*S^2-5897*S^3-6292*S^4-2687*S^5+194*S^6+467*S^7+82*S^8)/(128*(3+2*S)*(4+S)*(1-S)^6);
B(5,5) = 5*(300+1579*S+3176*S^2+2949*S^3+1188*S^4+675*S^5+1326*S^6+827*S^7+130*S^8)/(384*(3+2*S)*(4+S)*(1-S)^6);
    
    
% C coefficients
% Note that C(1) = C0, C(2) = C2, C(4) = C4
C = nan(4,1); % Initialise as NaN
C(1) = (tanh(k*d))^(1/2);  % = C0
C(2) = (tanh(k*d))^(1/2)*(2+7*S^2)/(4*(1-S)^2); % = C2
C(4) = (tanh(k*d))^(1/2)*(4+32*S-116*S^2-400*S^3-71*S^4+146*S^5)/(32*(1-S)^5); % = C4

